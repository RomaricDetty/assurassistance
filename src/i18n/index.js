const I18nContext = createContext({
    lang: FALLBACK_LANG,
    setLang: () => {},
    t: (key) => key
});

/** Lit une clé de traduction (ex: "nav.profile"). */
function getMessage(lang, key) {
    return key.split('.').reduce((acc, part) => (acc && acc[part] != null ? acc[part] : null), messages[lang]);
}

/** Remplace les variables {{...}} dans une chaîne traduite. */
function interpolate(text, vars = {}) {
    return Object.keys(vars).reduce(
        (acc, k) => acc.replaceAll(`{{${k}}}`, String(vars[k] ?? '')),
        text
    );
}

/** Fournit langue courante + traducteur à toute l'application. */
export const I18nProvider = ({ children }) => {
    const [lang, setLangState] = useState(() => {
        if (typeof window === 'undefined') return FALLBACK_LANG;
        const saved = localStorage.getItem(STORAGE_KEY);
        return saved === 'fr' || saved === 'en' ? saved : FALLBACK_LANG;
    });

    const setLang = useCallback((nextLang) => {
        const value = nextLang === 'fr' ? 'fr' : 'en';
        setLangState(value);
        if (typeof window !== 'undefined') localStorage.setItem(STORAGE_KEY, value);
    }, []);

    const t = useCallback((key, vars) => {
        const selected = getMessage(lang, key);
        const fallback = getMessage(FALLBACK_LANG, key);
        const raw = selected ?? fallback ?? key;
        return typeof raw === 'string' ? interpolate(raw, vars) : key;
    }, [lang]);

    const value = useMemo(() => ({ lang, setLang, t }), [lang, setLang, t]);

    return React.createElement(I18nContext.Provider, { value }, children);
};

/** Hook i18n à utiliser dans les composants React. */
export const useI18n = () => useContext(I18nContext);
